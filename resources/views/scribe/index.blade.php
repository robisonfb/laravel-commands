                                <h2 id="profile-POSTapi-v1-email-change">Send verification email for email change</h2>

<p>
<small class="badge badge-darkred">requires authentication</small>
</p>

<p>Send verification link to update user's email address with the new one</p>
<p><strong>Email Verification Process:</strong>
After sending, a verification email is automatically sent. When the user
clicks the verification link, they will be redirected to the frontend with different
status depending on the result:</p>
<ul>
<li><strong>Success</strong>: <code>{FRONTEND_URL}/verify-email?status=success&amp;message=Email+verified+successfully</code></li>
<li><strong>Error - Invalid ID</strong>: <code>{FRONTEND_URL}/verify-email?status=error&amp;message=Invalid+user+ID</code></li>
<li><strong>Error - User not found</strong>: <code>{FRONTEND_URL}/verify-email?status=error&amp;message=User+not+found</code></li>
<li><strong>Error - Invalid/expired link</strong>: <code>{FRONTEND_URL}/verify-email?status=error&amp;message=Invalid+or+expired+verification+link</code></li>
</ul>
<p><strong>Important:</strong> The backend manages the entire email verification process,
including redirects. The frontend must be prepared to receive the
<code>status</code> and <code>message</code> parameters in the URL and display appropriate messages.</p>

