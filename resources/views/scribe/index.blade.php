        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include an <strong><code>Authorization</code></strong> header with the value <strong><code>"Bearer {YOUR_TOKEN}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>You can obtain your token by logging in through the <code>POST api/v1/auth/login</code> endpoint. The token must be sent in the Authorization header as <code>Bearer {token}</code>.</p>

